sub first_release {
    my ($discard, $module, $version) = @_;

    my @perls = $version
        ? grep { exists $version{$_}{ $module } &&
                        $version{$_}{ $module } >= $version } keys %version
        : grep { exists $version{$_}{ $module }             } keys %version;

    return unless @perls;
    return (sort { $released{$a} cmp $released{$b} } @perls)[0];
}


